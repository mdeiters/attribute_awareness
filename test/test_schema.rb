ActiveRecord::Schema.define(:version => 1) do
  create_table "dogs", :force => true do |t|
    t.string   "name"
    t.integer  "age"
    t.datetime "created_at"
  end
  
  create_table "cats", :force => true do |t|
    t.string   "calling"
    t.boolean     "cranky"
    t.datetime "created_at"
  end
  
  create_table "birds", :force => true do |t|
    t.string   "name"
  end
  
end